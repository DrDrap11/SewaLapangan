    public ResultSet selectsewa() {
        try {
            String sql = "SELECT * FROM sewa";
            st = conn.createStatement();
            rs = st.executeQuery(sql);
        } catch (SQLException ex) {
            Logger.getLogger(Koneksi.class.getName()).log(Level.SEVERE, null, ex);
        }
        return rs;
    }
    public void showTablesewa() {
        try {
            tbmsewa = new DefaultTableModel(new String[]{"Lapangan", "Kode Lapangan", "Tanggal Pemesanan", "Jam Pemeseanan", "Lama Pemesanan", "Nama Pemesan", "Nama Team", "No Telepon"}, 0);
            rs = selectsewa();
            while (rs.next()) {
                tbmsewa.addRow(new Object[]{
                    rs.getString("lapangan"),
                    rs.getString("kode_lapangan"),
                    rs.getString("tanggal_pemesanan"),
                    rs.getString("jam_pemesanan"),
                    rs.getString("lama_pemesanan"),
                    rs.getString("nama_pemesan"),
                    rs.getString("nama_team"),
                    rs.getString("telepon")});
            }
        } catch (SQLException ex) {
            Logger.getLogger(Frame_booking_lapangan.class.getName()).log(Level.SEVERE, null, ex);
        }
        tblBarang.setModel(tbmsewa);
    }
