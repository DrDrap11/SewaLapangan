    private void btnPesanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesanActionPerformed
        String lapangan, kode_lapangan, jam_pemesanan, lama_pemesanan, nama_pemesan, nama_team, telepon;
        Date tanggal_pemesanan = null;
        try {
            lapangan = (String) lpg.getSelectedItem();
            kode_lapangan = (String) kode.getSelectedItem();
            jam_pemesanan = (String) Jam1.getSelectedItem();
            lama_pemesanan = (String) lama.getSelectedItem();
            nama_pemesan = txtNama.getText();
            nama_team = txtTeam.getText();
            telepon = txtNo.getText();
            try {
                if (lpg.getSelectedItem().equals("")) {
                    JOptionPane.showMessageDialog(null, "Lapangan Wajib Dipilih");
                } else if (kode.getSelectedItem().equals("")) {
                    JOptionPane.showMessageDialog(null, "Kode Wajib Dipilih");
                } else if (Jam1.getSelectedItem().equals("")) {
                    JOptionPane.showMessageDialog(null, "Jam Wajib Dipilih");
                } //            else if (jdcTanggal_Lahir.getDate().equals("")) {
                //                JOptionPane.showMessageDialog(null, "Tanggal Lahir Tidak Boleh Kosong");
                //            } 
                else if (lama.getSelectedItem().equals("")) {
                    JOptionPane.showMessageDialog(null, "Wajib Dipilih");
                } else if (txtNama.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "Nama Wajib Diisi");
                } else if (txtTeam.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "Nama Wajib Diisi");
                } else if (txtNo.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "No Handphone Wajib Diisi");
                } else {
                    int opsi = JOptionPane.showConfirmDialog(null, "Apakah anda ingin menyimpan data ini ?", "Simpan Data", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
                    switch (opsi) {
                        case JOptionPane.YES_OPTION:
                            insertsewa(lapangan, kode_lapangan, tanggal_pemesanan, jam_pemesanan, lama_pemesanan, nama_pemesan, nama_team, telepon);
                            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                            clearsewa();
                            break;
                        case JOptionPane.NO_OPTION:
                            JOptionPane.showMessageDialog(null, "Data tidak disimpan");
                            break;
                    }
                }
            } catch (Exception t) {
                JOptionPane.showMessageDialog(null, "Data Gagal Disimpan!");
            }
        } catch (Exception e) {
        }
        showTablesewa();
    }
public void insertsewa(String lapangan, String kode_lapangan, Date tanggal_pemesanan, String jam_pemesanan, String lama_pemesanan, String nama_pemesan, String nama_team, String telepon){        
try {
            String sql = "INSERT INTO sewa (lapangan, kode_lapangan, tanggal_pemesanan, jam_pemesanan, lama_pemesanan, nama_pemesan, nama_team, telepon) VALUES (?,?,?,?,?,?,?,?,)";
            pst = conn.prepareStatement(sql);
            pst.setString(1, (String) lpg.getSelectedItem());
            pst.setString(2, (String) kode.getSelectedItem());
            pst.setString(3, ((JTextField) tgl.getDateEditor().getUiComponent()).getText());
            pst.setString(4, (String) Jam1.getSelectedItem());
            pst.setString(5, (String) lama.getSelectedItem());
            pst.setString(6, txtNama.getText());
            pst.setString(7, txtTeam.getText());
            pst.setString(8, txtNo.getText());
            pst.execute();
        } catch (SQLException ex) {
            System.err.println(ex);
        }
    }//GEN-LAST:event_btnPesanActionPerformed

